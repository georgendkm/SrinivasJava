package classwork;

public class SwitchCaseDemo {

	public static void main(String[] args) {
		
		int i = 0;
		
		switch (i) {
		
		case 1 :
			System.out.println(" i am in case 1");
			System.out.println("one");
			break;
		case 2 :
			System.out.println(" i am in case 2");
			System.out.println("two");
			break;
		case 3 :
			System.out.println(" i am in case 3");
			System.out.println("three");
			break;
		case 4 :
			System.out.println(" i am in case 4");
			System.out.println("four");
			break;
		case 5 :
			System.out.println(" i am in case 5");
			System.out.println("five");
			break;
		case 6 :
			System.out.println(" i am in case 6");
			System.out.println("six");
			break;
		case 7 :
			System.out.println(" i am in case 7");
			System.out.println("seven");
			break;
		case 8 :
			System.out.println(" i am in case 8");
			System.out.println("eight");
			break;
		case 10 :
			System.out.println(" i am in case 10");
			System.out.println("ten");
			break;
		case 9 :
			System.out.println(" i am in case 9");
			System.out.println("nine");
			break;
		default :
			System.out.println(" invalid number");
		}
		

	}

}
